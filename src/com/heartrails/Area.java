package com.heartrails;

public class Area {
	private String prefecture;
	private String city;
	private String city_kana;
	private String town;
	private String town_kana;
	private String x;
	private String y;
	private String postal;

	public String getPrefecture() {
		return prefecture;
	}
	public String getCity() {
		return city;
	}
	public String getCity_kana() {
		return city_kana;
	}
	public String getTown() {
		return town;
	}
	public String getTown_kana() {
		return town_kana;
	}
	public String getX() {
		return x;
	}
	public String getY() {
		return y;
	}
	public String getPostal() {
		return postal;
	}
}
